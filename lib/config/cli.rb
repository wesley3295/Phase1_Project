        puts "Legend is an app that gives base statistics on Champions in the MOBA game League of Legends!"
        Api.data_of_champions
    main_menu
    end

    def main_menu
        puts "Type '1' for a list of Champions"
        puts "Type 'exit' to exit Legend"
        main_menu_options
    end

    def main_menu_options
            input = menu_input
         if input == "1"
    spaceing 
    champ_select_logo
    spaceing
            list_champions
            select_champion         
             elsif input == "exit"
                exits
         else
            main_menu
         end
    end

    def select_champion
        input = champion_input
        index = input.to_i - 1
        if index.between?(0, 152)
        champ = Champions.all[index]
        champ_base_stats(champ)
        elsif input == "exit"
            exits
        elsif
             index > 152
            invalid_option
        else
            invalid_option
        end
        select_champion
    end

    def champ_base_stats(champ)
            spaceing
            stats_logo
            spaceing
    puts "
         Name:#{champ.name}
         Title:#{champ.title}
         Difficulty:#{champ.difficulty}
         Magic Damage:#{champ.magic}
         Attack:#{champ.attack}
         Attack Damage:#{champ.attackdamage}
         Attack Range:#{champ.attackrange}
         Magic Resist:#{champ.spellblock}
         Armor:#{champ.armor}
         Defence:#{champ.defence}
         Health:#{champ.hp}
         Mana:#{champ.mp}
         Movement Speed:#{champ.movespeed}"
    end
  
  

    def menu_input
       print "Enter here:" 
       gets.chomp
    end
    
    def champion_input
        print "Type the index of champion here:"
        gets.chomp
    end

    def list_champions
        Champions.all.each.with_index(1) do |champion, index|
            puts "#{index}. #{champion.name}"
        end
        # champ_detail_menu
    end

    def invalid_option
        puts "Invalid input. Please try again!"
    end

    def exits
        puts "Thanks for chosing Legend!"
        exit
    end


